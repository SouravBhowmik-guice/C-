#include <iostream>
using namespace std;

int main(){
       int number1 = 0b00001111; //binary literal for 15
       int number2 = 0x0000000F; //hexadecimal literal for 15

       cout << "Number1 (binary 0b00001111) is: " << number1 << endl;
       cout << "Number2 (hexadecimal 0x0000000F) is: " << number2 << endl; 
       cout<<"--------------------------------"<<endl;
      
       return 0;
}